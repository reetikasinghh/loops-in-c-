/*QUES: WRITE A PROGRAM TO PRINT THE FIRST 50 NUMBERS USING LOOPS */
#include <iostream>
int main() {
    // Initialize a variable 'c' to 1
    for (int c = 1; c <= 50; c++) {
        // Output the current value of 'c' followed by a newline
        std::cout << c << std::endl;
    }

    // Return 0 to indicate successful program execution
    return 0;
}
